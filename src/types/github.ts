export interface GitHubRepository {
    id: number;
    name: string;
    full_name: string;
    description: string | null;
    html_url: string;
    language: string | null;
    stargazers_count: number;
    forks_count: number;
    updated_at: string;
    visibility: string;
    topics: string[];
    license: {
        name: string;
        spdx_id: string;
    } | null;
}

export interface GitHubApiResponse {
    repositories: GitHubRepository[];
    error?: string;
}
